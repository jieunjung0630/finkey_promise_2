  Widget build(BuildContext context) {
    return Scaffold(
      body: Center(
        child: Column(
          mainAxisAlignment: MainAxisAlignment.center,
          children: <Widget>[
            Image.asset(
              'assets/logo.png', // Update this path to the correct path of your logo
              width: 200.0, // Adjust the width as needed
              height: 200.0, // Adjust the height as needed
            ),
            SizedBox(height: 20),
            ElevatedButton(
